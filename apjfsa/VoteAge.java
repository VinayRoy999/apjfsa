package com.apjfsa;

public class VoteAge {
	
	
    public static void main(String[] args) {
        
    	
    	int age = 25;
        int vage = 18;

       
        // Compare age and vage to check voting eligibility     
        
        if (age >= vage) {
            System.out.println("Old enough to vote!");
        } 
        
        
        else {
            System.out.println("Not old enough to vote.");
      
        
        }
    }
}
